package day_two

fun main() {
    // Using If Statements
    var language: String = "FR"

    if (language === "EN") {
        println("Hello World!")
    } else if (language === "ES") {
        println("Hola Mundo!")
    } else if (language === "FR") {
        println("Cava bein")
    } else {
        println("We greet you, ha mi pfuxela")
    }
}